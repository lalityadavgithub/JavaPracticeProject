package PracticeSet;

public class Ch16_Conditional {
    public static void main(String[] args) {
        int age =18;
        String name = "Rohan";
        if(age!=18){
            System.out.println("You can drive");
        }
        else{
            System.out.println("You can not drive");
        }

        if (name.equals("Rohan")){
            System.out.println("You are Rohan");
        }
        else{
            System.out.println("You are not Rohan");
        }
    }

}
